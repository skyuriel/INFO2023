# Divide dos numeros e imprime su cociente y resto #
nro_1 = int(input('Ingresa el primer numero: '))
nro_2 = int(input('Ingresa el segundo numero: '))
cociente = int(nro_1 / nro_2)
resto = nro_1 % nro_2
print('El cociente de la division entre ' + str(nro_1) + ' y ' + str(nro_2) + ' es: ' + str(cociente) + ', \nel resto es: ' + str(resto))

